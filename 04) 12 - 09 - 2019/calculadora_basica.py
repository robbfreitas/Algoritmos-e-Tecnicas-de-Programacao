x = int(input('Digite o primeiro numero: '))
y = int(input('Digite o segundo numero: '))
op = input('Operador (+, -, *, /): ')
if op == '+':
        print(x+y)
elif op == '-':
        print(x-y)
elif op == '*':
        print(x*y)
elif op == '/':
        print(x-y)
else:
    print('Operador desconhecido')
